package entity;

/**
 *
 * @author Admin
 */
public class News {

    private int id;
    private String url;
    private String address;
    private String title;

    public News(int id, String url, String address, String title) {
        this.id = id;
        this.url = url;
        this.address = address;
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
    
    
    
    

}
